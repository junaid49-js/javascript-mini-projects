let addition = function(a,b){
    return a + b;
}

let substraction = function(a,b){
    return a - b;
}

let multiply = function(a,b){
    return a * b;
}

let division = function(a,b){
    return a / b;
}

let remainder = function(a,b){
    return a % b;
}

let squareOfX = function(x){
    return x * x;
}

let powerBy = function(a,b){
    return a ** b;
}


